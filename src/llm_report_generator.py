"""
llm_report_generator.py

Reads llm_summary_input.md files generated by spike_explainer.py,
sends the context to an LLM, and generates a final readable analytical 
report for each game.

Supports a flexible provider adapter architecture:
- Gemini
- OpenAI
- Anthropic Claude
- OpenAI-compatible (OpenRouter, Groq, Together, Ollama, LM Studio)
- Custom HTTP endpoints
"""

